export default function KnitSantaHat() {
  return (
    <div style={{ padding: "20px", fontFamily: "Arial, sans-serif" }}>
      <h1>How to Knit a Santa Hat</h1>
      <p>
        Welcome! This guide will help you knit a beautiful Santa hat step by
        step. Grab your knitting needles, yarn, and let’s get started!
      </p>

      <h2>Materials Needed</h2>
      <ul>
        <li>Red yarn (medium weight)</li>
        <li>White yarn (medium weight)</li>
        <li>Knitting needles (size 4 mm)</li>
        <li>Scissors</li>
        <li>Tapestry needle</li>
        <li>Pom-pom maker (optional)</li>
      </ul>

      <h2>Step-by-Step Instructions</h2>
      <ol>
        <li>
          <strong>Cast On:</strong> Start by casting on 60 stitches using the
          red yarn.
        </li>
        <li>
          <strong>Ribbing:</strong> Work in a K1, P1 ribbing pattern for 10
          rows.
        </li>
        <li>
          <strong>Body of the Hat:</strong> Switch to stockinette stitch (knit
          one row, purl one row) and continue for 25 rows.
        </li>
        <li>
          <strong>Decreasing:</strong> Begin decreasing by knitting two stitches
          together (K2tog) every 10 stitches. Continue decreasing every other
          row until 5 stitches remain.
        </li>
        <li>
          <strong>Bind Off:</strong> Cut the yarn, leaving a long tail. Use the
          tapestry needle to thread through the remaining stitches and pull
          tight to close the top of the hat.
        </li>
        <li>
          <strong>Add the White Band:</strong> Pick up stitches along the base
          of the hat using white yarn. Work in a K1, P1 ribbing pattern for 5
          rows. Bind off.
        </li>
        <li>
          <strong>Make the Pom-Pom:</strong> Use white yarn to create a pom-pom
          and attach it to the top of the hat.
        </li>
      </ol>

      <h2>Tips</h2>
      <ul>
        <li>Use soft yarn for a more comfortable fit.</li>
        <li>
          Experiment with different sizes by adjusting the number of cast-on
          stitches.
        </li>
      </ul>

      <h2>Enjoy!</h2>
      <p>
        You’ve made a cozy Santa hat perfect for the holiday season. Happy
        knitting!
      </p>
    </div>
  );
}
